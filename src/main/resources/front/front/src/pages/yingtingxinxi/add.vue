  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            yingtingbianhao : false,
            yingtingmingcheng : false,
            yingtingweizhi : false,
            zhunbeishangying : false,
            xuncharenyuan : false,
            yingtingshebei : false,
            yingtingrongliang : false,
            yingtingjieshao : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          yingtingbianhao: '',
          yingtingmingcheng: '',
          yingtingweizhi: '',
          zhunbeishangying: '',
          xuncharenyuan: '',
          yingtingshebei: '',
          yingtingrongliang: '',
